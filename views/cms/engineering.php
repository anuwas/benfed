            <p>The Engineering Division of BENFED renders technical consultancy in execution of engineering projects sanctioned under different Govt. Schemes like RKVY, RIDF, State Plan, NCDC etc. in the cooperative sector. These projects range from establishment of cold storages of different capacities, including multi-purpose cold storages, and construction of storage godowns of varying sizes to establishment of agricultural processing units like rice mills, oil mills, seed processing units etc. The engineering division of BENFED had, in the past, rendered technical consultancy for establishment of government-aided cold storages in other states like Meghalaya, Orissa and Jharkhand. Besides, the engineering division also supervises the construction and maintenance works of BENFED’s own establishments like godowns, cold storages, office buildings etc.</p>
		
			<p><strong>The responsibility of Engineering Division includes the following:</strong></p>
			 <ul class="list-style">
			<li><i class="fa fa-angle-double-right" aria-hidden="true"></i> Preparation of detailed project reports of different schemes.</li>
<li><i class="fa fa-angle-double-right" aria-hidden="true"></i> Preparation of economic design of buildings and plant & machinery with detailed drawings and estimates.</li>
<li><i class="fa fa-angle-double-right" aria-hidden="true"></i> Preparation of tender documents containing technical specifications, terms and conditions of contract of the respective civil, insulation, refrigeration, electrical work, Generator and the prime mover sets.</li>
