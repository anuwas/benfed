<li><i class="fa fa-angle-double-right" aria-hidden="true"></i> Inspection and supervision of work periodically during construction period and defect liability period.</li>
<li><i class="fa fa-angle-double-right" aria-hidden="true"></i> Checking of contractor’s bills and making recommendation for payment as per contract terms.</li>
