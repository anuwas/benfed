            <p>The marketing division of BENFED is mainly engaged in procurement of paddy from different farmers of the state of West Bengal through various societies enlisted with BENFED. This mechanism is performed under MSP (Minimum Support Price) of the Government of India.  MSP is a form of market intervention by the Government of India to insure agricultural producers against any sharp fall in farm prices. The MSP are announced at the beginning of the sowing season of certain crops in the basis of the recommendations of the Commission of Agricultural Costs and Prices (CACP). It thus helps the producers/farmers against excessive fall in price during high production years, the major objective being to support the farmers from distress sales. The department is also engaged in procurement of other agricultural produce like pulses, vegetables(mainly potato), grains, spices  etc. by ensuring support price to the respective farmers and also by selling at market price in the open market or by supplying them to the Government agencies. Under KMS (2015-16) more than 2 lakh farmers are benefited through the help of BENFED. The primary focus of the marketing team is to provide remunerative price to the farmers and supply to the consumers at moderate rate.</p>
		
              
